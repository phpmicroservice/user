<?php


namespace app\validation;

use app\validator\user_exist;

/**
 * 用户信息验证
 * Class edit_info
 * @package app\validation
 */
class edit_info extends \pms\Validation
{
    protected function initialize()
    {
        # 验证user_id
        $this->add_Validator('user_id', [
            'name' => user_exist::class,
            'message' => 'exist'
        ]);
        #  gender 性别
        $this->add_in('gender', [
            'domain' => [
                2, 1, -1
            ],
            'message' => 'gender'
        ]);
        # birthday 生日
        $this->add_date('birthday', [
            'format' => 'Y-m-d',
            'message' => 'date'
        ]);
        #  personalized 个性签名
        $this->add_stringLength('personalized', [
            'min' => 0,
            'max' => 50,
            'message' => 'stringLength'
        ]);

        return parent::initialize(); // TODO: Change the autogenerated stub
    }

}